void SystemClock_Config(void);
static void MX_GPIO_Init(void);

#define LED_PERIODS_QTY 2
#define LED_PERIOD_1_MS 100
#define LED_PERIOD_2_MS 500

static const uint32_t AVAILABLE_PERIODS[LED_PERIODS_QTY] =
{
    LED_PERIOD_1_MS,
    LED_PERIOD_2_MS
};

static void button_pressed()
{
    uart_send_string((uint8_t*)"BUTTON_FALLING\r\n");
}

static void button_released()
{
    uart_send_string((uint8_t*)"BUTTON_RAISING\r\n");
}

static button_handlers_t app_button_fsm_handlers =
{
        .pressed_cb = &button_pressed,
        .released_cb = &button_released,
};


/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
    delay_t delay = {};
    uint8_t led_period_index = 0;

    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_USB_DEVICE_Init();

    if(!uart_init()) {
        Error_Handler();
    }

    uart_send_string((uint8_t*)"Application begins here:\r\n");

    delay_init(&delay, AVAILABLE_PERIODS[led_period_index]);
    debounce_fsm_init(&app_button_fsm_handlers);

    while (1)
    {
        debounce_fsm_update();

        if(read_key()) {
            led_period_index = (led_period_index + 1) % LED_PERIODS_QTY; // Wrap led_period_index between [0-LED_PERIODS_QTY]
        }

        delay_write(&delay, AVAILABLE_PERIODS[led_period_index]);

        if(delay_read(&delay)) {
            led_toggle(LED2);
        }
    }
}

